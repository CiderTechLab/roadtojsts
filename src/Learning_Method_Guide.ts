/**
 * ==========================================
 * 6. つまずきやすいポイントと対策
 * ==========================================
 * 
 * 【よくあるつまずきポイント】
 * 
 * ■ ポイント1: Union Typesでのメソッド呼び出し
 * 
 * ❌ 問題：
 * function printLength(value: string | number) {
 *   console.log(value.length); // エラー！numberにlengthはない
 * }
 * 
 * ✅ 解決：Type Guardを使う
 * function printLength(value: string | number) {
 *   if (typeof value === 'string') {
 *     console.log(value.length); // OK！stringに絞り込まれた
 *   }
 * }
 * 
 * ■ ポイント2: anyの乱用
 * 
 * ❌ 避けるべき：
 * function process(data: any): any {
 *   // anyを使うとTypeScriptの恩恵を受けられない
 * }
 * 
 * ✅ 推奨：具体的な型を定義
 * function process(data: InputData): OutputData {
 *   // 型安全！
 * }
 * 
 * もしくは、unknownを使う：
 * function process(data: unknown): OutputData {
 *   if (isInputData(data)) { // Type Guardで検証
 *     // ここでは安全にdataを使える
 *   }
 * }
 * 
 * ■ ポイント3: オブジェクトのプロパティアクセス
 * 
 * ❌ 問題：
 * interface User {
 *   name: string;
 * }
 * 
 * const user: User = { name: "Alice" };
 * console.log(user.age); // エラー！ageはUserに存在しない
 * 
 * ✅ 解決：Optional propertyを使う
 * interface User {
 *   name: string;
 *   age?: number; // オプショナル
 * }
 * 
 * const user: User = { name: "Alice" };
 * console.log(user.age); // OK（undefinedの可能性あり）
 * 
 * // 安全な使い方
 * if (user.age !== undefined) {
 *   console.log(user.age);
 * }
 * 
 * ■ ポイント4: 配列とタプルの混同
 * 
 * 配列（すべて同じ型）：
 * const numbers: number[] = [1, 2, 3];
 * 
 * タプル（位置ごとに型が決まる）：
 * const person: [string, number] = ["Alice", 30];
 * // person[0]はstring、person[1]はnumber
 * 
 * ■ ポイント5: thisの扱い
 * 
 * ❌ 問題：アロー関数でないメソッド
 * class Counter {
 *   count = 0;
 *   
 *   increment() { // 通常の関数
 *     this.count++;
 *   }
 * }
 * 
 * const counter = new Counter();
 * const inc = counter.increment;
 * inc(); // エラー！thisがundefined
 * 
 * ✅ 解決1：アロー関数を使う
 * class Counter {
 *   count = 0;
 *   
 *   increment = () => { // アロー関数
 *     this.count++;
 *   }
 * }
 * 
 * ✅ 解決2：bindする
 * const inc = counter.increment.bind(counter);
 * inc(); // OK
 */

/**
 * ==========================================
 * 7. デバッグとエラー解決のテクニック
 * ==========================================
 * 
 * 【エラーメッセージの読み方】
 * 
 * TypeScriptのエラーメッセージは以下の構造：
 * 
 * src/file.ts:10:5 - error TS2339: Property 'x' does not exist on type 'Y'.
 * ^^^^^^^^^^^  ^^^^    ^^^^^^^^^  ^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^^
 * ファイル名   行:列   エラーコード  エラーメッセージ
 * 
 * ■ エラーコードの活用：
 * - TS2339: プロパティが存在しない
 * - TS2345: 引数の型が合わない
 * - TS2322: 代入する型が合わない
 * - TS7006: 暗黙のany型
 * 
 * エラーコードで検索すると詳細な情報が見つかる
 * 
 * 【デバッグの手順】
 * 
 * Step 1: エラーメッセージを正確に読む
 * - どのファイルの何行目か
 * - 何が問題なのか
 * 
 * Step 2: 型を確認する
 * // マウスホバーで型を確認（VSCodeの場合）
 * // または、以下のようにして型を表示
 * type DebugType = typeof someVariable;
 * 
 * Step 3: 段階的に確認
 * - まず最もシンプルな形で動かす
 * - 徐々に複雑にしていく
 * - どこで問題が起きるか特定
 * 
 * Step 4: TypeScript Playgroundで試す
 * - 問題の部分だけを切り出す
 * - オンラインで素早く検証
 * 
 * 【役立つコマンド】
 * 
 * // 型チェックのみ（コンパイルしない）
 * tsc --noEmit
 * 
 * // より詳細なエラー情報
 * tsc --pretty --extendedDiagnostics
 * 
 * // 特定のファイルだけチェック
 * tsc src/file.ts --noEmit
 */

